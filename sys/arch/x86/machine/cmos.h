#ifndef __MACHINE_CMOS_H
#define __MACHINE_CMOS_H

enum {
    CMOS_IO = 0x70
};

enum {
    CMOS_REG_FDC    = 0x10
};

#ifdef __KERNEL

inline static uint8_t
cmos_rdreg(int re)
{
    io_out8(CMOS_IO, re);
    return io_in8(CMOS_IO+1);
}

inline static void
cmos_wrreg(int re, uint8_t v)
{
    io_out8(CMOS_IO, re);
    io_out8(CMOS_IO+1, v);
}

#endif

#endif


